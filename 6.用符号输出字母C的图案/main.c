#include <stdio.h>
#include <stdlib.h>

//题目：用*号输出字母C的图案

int main()
{
    char _signed[11][9]=
    {
        {' ',' ','*','*','*','*','*','*',' '},
        {' ','*','*','*','*','*','*','*','*'},
        {'*','*',' ',' ',' ',' ',' ','*','*'},
        {'*',' ',' ',' ',' ',' ',' ','*','*'},
        {'*',' ',' ',' ',' ',' ',' ',' ',' '},
        {'*',' ',' ',' ',' ',' ',' ',' ',' '},
        {'*',' ',' ',' ',' ',' ',' ',' ',' '},
        {'*',' ',' ',' ',' ',' ',' ','*','*'},
        {'*','*',' ',' ',' ',' ',' ','*','*'},
        {' ','*','*','*','*','*','*','*','*'},
        {' ',' ','*','*','*','*','*','*',' '}
    };

    int a,b;
    for(a=0;a<11;a++)
    {
        for(b=0;b<9;b++)
        {
            printf("%c",*(*(_signed+a)+b));
        }
        printf("\n");
    }

    return 0;
}
